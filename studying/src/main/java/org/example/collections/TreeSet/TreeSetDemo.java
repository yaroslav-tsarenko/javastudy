package org.example.collections.TreeSet;

import java.util.TreeSet;

public class TreeSetDemo {

    public static void main(String args[]) {
        // Создаем набор древ
        TreeSet ts = new TreeSet();

        // Добавляем элементы к набору древ
        ts.add("C");
        ts.add("A");
        ts.add("B");
        ts.add("E");
        ts.add("F");
        ts.add("D");
        System.out.println(ts);
    }
}
